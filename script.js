// وظيفة بدء الاختبار
function startQuiz() {
    if (questions.length === 0) {
        alert("برجاء إضافة الأسئلة أولاً! (تجد مكان إضافة الأسئلة في ملف script.js)");
        return;
    }
    startScreen.style.display = 'none';
    quizContainer.style.display = 'block';
    currentQuestionIndex = 0;
    score = 0;
    userAnswers = [];
    totalQuestionsSpan.textContent = questions.length;
    loadQuestion();
}

// وظيفة تحميل السؤال الحالي
function loadQuestion() {
    if (currentQuestionIndex < questions.length) {
        const question = questions[currentQuestionIndex];
        currentQuestionNumberSpan.textContent = currentQuestionIndex + 1;
        questionText.textContent = question.question;
        optionsContainer.innerHTML = ''; // مسح الخيارات السابقة
        explanationDiv.style.display = 'none';
        nextButton.style.display = 'none';

        question.options.forEach(option => {
            const button = document.createElement('button');
            button.textContent = option;
            button.addEventListener('click', () => selectAnswer(option, question.correctAnswer, question.explanation));
            optionsContainer.appendChild(button);
        });
    } else {
        showResults(); // عرض النتائج عند الانتهاء من جميع الأسئلة
    }
}

// وظيفة اختيار الإجابة
function selectAnswer(selectedOption, correctAnswer, explanation) {
    const optionsButtons = optionsContainer.querySelectorAll('button');
    let isCorrect = (selectedOption === correctAnswer);

    userAnswers.push({
        question: questions[currentQuestionIndex].question,
        selected: selectedOption,
        correct: correctAnswer,
        explanation: explanation,
        isCorrect: isCorrect
    });

    optionsButtons.forEach(button => {
        button.disabled = true; // تعطيل جميع الأزرار بعد الاختيار
        if (button.textContent === correctAnswer) {
            button.classList.add('correct');
        } else if (button.textContent === selectedOption) {
            button.classList.add('incorrect');
        }
    });

    if (isCorrect) {
        score++;
    }

    explanationText.textContent = explanation;
    explanationDiv.style.display = 'block';
    nextButton.style.display = 'block';
}

// وظيفة تحميل السؤال التالي
function loadNextQuestion() {
    currentQuestionIndex++;
    loadQuestion();
}

// وظيفة عرض النتائج
function showResults() {
    quizContainer.style.display = 'none';
    resultsContainer.style.display = 'block';
    scoreDisplay.textContent = score;
    maxScoreDisplay.textContent = questions.length;
    displayReviewedQuestions();
}

// وظيفة عرض الأسئلة مع الإجابات الصحيحة والخاطئة والتفسيرات
function displayReviewedQuestions() {
    reviewQuestionsDiv.innerHTML = '';
    userAnswers.forEach((answer, index) => {
        const item = document.createElement('div');
        item.classList.add('review-question-item');
        item.innerHTML = `
            <p class="question-text"><strong>السؤال ${index + 1}:</strong> ${answer.question}</p>
            <p class="user-answer"><strong>إجابتك:</strong> <span style="color: ${answer.isCorrect ? '#98FB98' : '#FF6347'};">${answer.selected}</span></p>
            <p class="correct-answer"><strong>الإجابة الصحيحة:</strong> ${answer.correct}</p>
            <p class="explanation-text"><strong>التفسير:</strong> ${answer.explanation}</p>
        `;
        reviewQuestionsDiv.appendChild(item);
    });
}

// وظيفة إرسال النتائج عبر واتساب
function sendResultsViaWhatsApp() {
    const phoneNumber = whatsappNumberInput.value.trim();
    if (!phoneNumber) {
        alert("الرجاء إدخال رقم الواتساب الخاص بك.");
        return;
    }

    // تحقق أساسي لرقم الهاتف (يمكن تحسينه)
    if (!/^\+\d{10,15}$/.test(phoneNumber)) {
        alert("الرجاء إدخال رقم هاتف صالح بالصيغة الدولية (مثال: +201234567890).");
        return;
    }

    let message = `نتائج اختبارك:\n\nدرجتك: ${score} من ${questions.length}\n\n`;
    message += "تفاصيل الإجابات:\n\n";

    userAnswers.forEach((answer, index) => {
        message += `السؤال ${index + 1}: ${answer.question}\n`;
        message += `إجابتك: ${answer.selected} ${answer.isCorrect ? '(صحيح ✅)' : '(خطأ ❌)'}\n`;
        message += `الإجابة الصحيحة: ${answer.correct}\n`;
        message += `التفسير: ${answer.explanation}\n\n`;
    });

    const whatsappUrl = `https://wa.me/${phoneNumber}?text=${encodeURIComponent(message)}`;
    window.open(whatsappUrl, '_blank');
}

// وظيفة إعادة الاختبار
function restartQuiz() {
    resultsContainer.style.display = 'none';
    startQuiz(); // العودة لشاشة البدء أو بدء الاختبار مباشرة
}

